import java.util.*;

public class Main {
  static class Pair{
    int a;
    int b;
    public Pair(int a,int b)
    {
      this.a=a;
      this.b=b;
    }
  }
    public static void main(String[] args) {
       Scanner scn=new Scanner(System.in);
       int val1=scn.nextInt();
       int val2=scn.nextInt();
       Pair cons=new Pair(val1,val2);
       System.out.println(car(cons)+" "+cdr(cons));
    }
    public static int car(Pair cons)
    {
       return cons.a;
    }
    public static int cdr(Pair cons)
    {
       return cons.b;
    }
}
